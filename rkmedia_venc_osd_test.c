// 绘制一个字符到位图
// 绘制一个字符到位图
// 修改后的 draw_char 和 draw_string
static void draw_char(RK_U32 *buffer, int x, int y, int width, int height, char c, RK_U32 color) {
    int char_index = -1;
    if (c >= '0' && c <= '9') {
        char_index = c - '0';
    } else if (c == ':') {
        char_index = 10;
    } else if (c == '-') {
        return;  // 假设 font_32x32 支持 '-'，请确认索引
    } else if (c == ' ') {
        return; // 空格不绘制
    } else {
        return; // 不支持的字符
    }

    const unsigned char *bitmap = font_32x32[char_index]; // 指向 128 字节数据
    for (int row = 0; row < 32; row++) {                  // 32 行
        int base_byte = row * 4; // 每行占 4 字节
        for (int col = 0; col < 32; col++) {              // 32 列
            int byte_offset = base_byte + (col / 8);       // 字节在行内的偏移
            int bit = 7 - (col % 8);                       // 位在字节内的位置（从左到右）
            if (bitmap[byte_offset] & (1 << bit)) {        // 该位为 1，则绘制像素
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < width && py >= 0 && py < height) {
                    buffer[py * width + px] = color;
                }
            }
        }
    }
}

static void draw_string(RK_U32 *buffer, int x, int y, int width, int height ,const char *str, RK_U32 color) {
    int current_x = x;
    for (int i = 0; str[i] != '\0'; i++) {
        draw_char(buffer, current_x, y, width, height, str[i], color);
        current_x += 25;  // 每个字符宽 32 像素，紧密排列
        // 可选：加空隙 current_x += 36; // 32 + 4 像素间距
    }
}
// 获取当前时间字符串
static void get_time_string(char *buffer, int buffer_size) {
    time_t rawtime;
    struct tm *timeinfo;
    
    time(&rawtime);
    timeinfo = localtime(&rawtime);
    
    strftime(buffer, buffer_size, "%Y-%m-%d %H:%M:%S", timeinfo);
}

static void set_argb8888_buffer(RK_U32 *buf, RK_U32 size, RK_U32 color) {
  for (RK_U32 i = 0; buf && (i < size); i++)
    *(buf + i) = color;
}

static void *GetMediaBuffer(void *arg) {
  printf("#Start %s thread, arg:%p\n", __func__, arg);

  MEDIA_BUFFER mb = NULL;
  while (!quit) {
    mb = RK_MPI_SYS_GetMediaBuffer(RK_ID_VENC, 0, -1);
    if (!mb) {
      printf("RK_MPI_SYS_GetMediaBuffer get null buffer!\n");
      break;
    }
    if (g_save_file)
      fwrite(RK_MPI_MB_GetPtr(mb), 1, RK_MPI_MB_GetSize(mb), g_save_file);
    RK_MPI_MB_ReleaseBuffer(mb);
  }

  return NULL;
}

static RK_CHAR optstr[] = "?::a::o:I:M:";
static const struct option long_options[] = {
    {"aiq", optional_argument, NULL, 'a'},
    {"help", optional_argument, NULL, '?'},
    {"output", required_argument, NULL, 'o'},
    {"camid", required_argument, NULL, 'I'},
    {"multictx", required_argument, NULL, 'M'},
    {NULL, 0, NULL, 0},
};

static void print_usage(const RK_CHAR *name) {
  printf("usage example:\n");
#ifdef RKAIQ
  printf("\t%s [-a [iqfiles_dir]] "
         "[-I 0] "
         "[-M 0] "
         "[-o output.h264]\n",
         name);
  printf("\t-a | --aiq: enable aiq with dirpath provided, eg:-a "
         "/oem/etc/iqfiles/, "
         "set dirpath empty to using path by default, without this option aiq "
         "should run in other application\n");
  printf("\t-M | --multictx: switch of multictx in isp, set 0 to disable, set "
         "1 to enable. Default: 0\n");
#else
  printf("\t%s [-o output.h264] [-I 0]\n", name);
#endif
  printf("\t-o | --output: output path, Default:/tmp/output.h264\n");
  printf("\t-I | --camid: camera ctx id, Default 0\n");
}

int main(int argc, char *argv[]) {
  int ret = 0;
  RK_CHAR *pOutPath = "/tmp/output.h264";
  int video_width = 1920;
  int video_height = 1080;
  int c;
  RK_S32 s32CamId = 0;
#ifdef RKAIQ
  RK_BOOL bMultictx = RK_FALSE;
#endif
  char *iq_file_dir = NULL;
  while ((c = getopt_long(argc, argv, optstr, long_options, NULL)) != -1) {
    const char *tmp_optarg = optarg;
    switch (c) {
    case 'a':
      if (!optarg && NULL != argv[optind] && '-' != argv[optind][0]) {
        tmp_optarg = argv[optind++];
      }
      if (tmp_optarg) {
        iq_file_dir = (char *)tmp_optarg;
      } else {
        iq_file_dir = "/oem/etc/iqfiles/";
      }
      break;
    case 'o':
      pOutPath = optarg;
      break;
    case 'I':
      s32CamId = atoi(optarg);
      break;
#ifdef RKAIQ
    case 'M':
      if (atoi(optarg)) {
        bMultictx = RK_TRUE;
      }
      break;
#endif
    case '?':
    default:
      print_usage(argv[0]);
      return 0;
    }
  }

  signal(SIGINT, sigterm_handler);

  printf("#output path: %s\n", pOutPath);
  printf("#CameraIdx: %d\n\n", s32CamId);
  g_save_file = fopen(pOutPath, "w");
  if (!g_save_file) {
    printf("WARN: open %s failed!\n", pOutPath);
  }
  if (iq_file_dir) {
#ifdef RKAIQ
    printf("#Rkaiq XML DirPath: %s\n", iq_file_dir);
    printf("#bMultictx: %d\n\n", bMultictx);
    rk_aiq_working_mode_t hdr_mode = RK_AIQ_WORKING_MODE_NORMAL;
    int fps = 30;
    SAMPLE_COMM_ISP_Init(s32CamId, hdr_mode, bMultictx, iq_file_dir);
    SAMPLE_COMM_ISP_Run(s32CamId);
    SAMPLE_COMM_ISP_SetFrameRate(s32CamId, fps);
#endif
  }

  RK_MPI_SYS_Init();
  VI_CHN_ATTR_S vi_chn_attr;
  vi_chn_attr.pcVideoNode = "rkispp_scale0";
  vi_chn_attr.u32BufCnt = 3;
  vi_chn_attr.u32Width = video_width;
  vi_chn_attr.u32Height = video_height;
  vi_chn_attr.enPixFmt = IMAGE_TYPE_NV12;
  vi_chn_attr.enWorkMode = VI_WORK_MODE_NORMAL;
  ret = RK_MPI_VI_SetChnAttr(s32CamId, 0, &vi_chn_attr);
  ret |= RK_MPI_VI_EnableChn(s32CamId, 0);
  if (ret) {
    printf("ERROR: create VI[0] error! ret=%d\n", ret);
    return 0;
  }

  VENC_CHN_ATTR_S venc_chn_attr;
  memset(&venc_chn_attr, 0, sizeof(venc_chn_attr));
  venc_chn_attr.stVencAttr.enType = RK_CODEC_TYPE_H264;
  venc_chn_attr.stVencAttr.imageType = IMAGE_TYPE_NV12;
  venc_chn_attr.stVencAttr.u32PicWidth = video_width;
  venc_chn_attr.stVencAttr.u32PicHeight = video_height;
  venc_chn_attr.stVencAttr.u32VirWidth = video_width;
  venc_chn_attr.stVencAttr.u32VirHeight = video_height;
  venc_chn_attr.stVencAttr.u32Profile = 77;
  venc_chn_attr.stRcAttr.enRcMode = VENC_RC_MODE_H264CBR;
  venc_chn_attr.stRcAttr.stH264Cbr.u32Gop = 30;
  venc_chn_attr.stRcAttr.stH264Cbr.u32BitRate = 2000000; // 2Mb
  venc_chn_attr.stRcAttr.stH264Cbr.fr32DstFrameRateDen = 0;
  venc_chn_attr.stRcAttr.stH264Cbr.fr32DstFrameRateNum = 30;
  venc_chn_attr.stRcAttr.stH264Cbr.u32SrcFrameRateDen = 0;
  venc_chn_attr.stRcAttr.stH264Cbr.u32SrcFrameRateNum = 30;
  ret = RK_MPI_VENC_CreateChn(0, &venc_chn_attr);
  if (ret) {
    printf("ERROR: create VENC[0] error! ret=%d\n", ret);
    return 0;
  }

  pthread_t read_thread;
  pthread_create(&read_thread, NULL, GetMediaBuffer, NULL);

  ret = RK_MPI_VENC_RGN_Init(0, NULL);
  if (ret) {
    printf("ERROR: RK_MPI_VENC_RGN_Init failed! ret=%d\n", ret);
    return -1;
  }

  // Bind VI and VENC
  MPP_CHN_S stSrcChn;
  stSrcChn.enModId = RK_ID_VI;
  stSrcChn.s32DevId = s32CamId;
  stSrcChn.s32ChnId = 0;
  MPP_CHN_S stDestChn;
  stDestChn.enModId = RK_ID_VENC;
  stDestChn.s32DevId = 0;
  stDestChn.s32ChnId = 0;
  ret = RK_MPI_SYS_Bind(&stSrcChn, &stDestChn);
  if (ret) {
    printf("ERROR: Bind VI[0] and VENC[0] error! ret=%d\n", ret);
    return 0;
  }
  printf("%s initial finish\n", __func__);

  // 创建OSD位图 - 确保尺寸16字节对齐
int osd_width = 640;
int osd_height = 64;
  int wxh_size = osd_width * osd_height;
  BITMAP_S BitMap;

  BitMap.enPixelFormat = PIXEL_FORMAT_ARGB_8888;
  BitMap.u32Width = osd_width;
  BitMap.u32Height = osd_height;
  BitMap.pData = malloc(wxh_size * TEST_ARGB32_PIX_SIZE);
  if (!BitMap.pData) {
    printf("ERROR: no mem left for argb8888(%d)!\n", wxh_size * TEST_ARGB32_PIX_SIZE);
    return -1;
  }

  // 设置OSD区域属性 - 确保位置和尺寸16字节对齐
  OSD_REGION_INFO_S RngInfo;
  RngInfo.enRegionId = REGION_ID_0;
  RngInfo.u32PosX = 16;    // 16 % 16 = 0，符合要求
  RngInfo.u32PosY = 16;    // 16 % 16 = 0，符合要求
  RngInfo.u32Width = osd_width;
  RngInfo.u32Height = osd_height;
  RngInfo.u8Enable = 1;
  RngInfo.u8Inverse = 0;


 printf("Start OSD display with text and time...\n");


  // 然后测试时间显示
  while (!quit) {
    // 使用纯色背景而不是半透明背景
    set_argb8888_buffer((RK_U32 *)BitMap.pData, wxh_size, TEST_ARGB32_TRANS);
    
    // 获取当前时间
    char time_str[32];
    get_time_string(time_str, sizeof(time_str));
    
    // 绘制时间字符串（白色）
    draw_string((RK_U32 *)BitMap.pData, 10,10, osd_width, osd_height,time_str, TEST_ARGB32_RED);
    
    // 更新OSD
    ret = RK_MPI_VENC_RGN_SetBitMap(0, &RngInfo, &BitMap);
    if (ret) {
      printf("ERROR: set rgn bitmap failed! ret=%d\n", ret);
      break;
    }
    
    printf("OSD updated: %s\n", time_str);
    
    // 每秒更新一次
    sleep(1);
  }

  printf("%s exit!\n", __func__);
  
  // 禁用OSD区域
  RngInfo.u8Enable = 0;
  RK_MPI_VENC_RGN_SetBitMap(0, &RngInfo, &BitMap);
  
  // 释放资源
  if (BitMap.pData)
    free(BitMap.pData);
  
  RK_MPI_SYS_UnBind(&stSrcChn, &stDestChn);
  RK_MPI_VENC_DestroyChn(0);
  RK_MPI_VI_DisableChn(s32CamId, 0);

#ifdef RKAIQ
  SAMPLE_COMM_ISP_Stop(s32CamId);
#endif

  if (g_save_file)
    fclose(g_save_file);

  return 0;

}
